int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	result;

	if (n == 0)
		return (0);
	while (*s1 == *s2 && n > 1)
	{
		if (*s1 == '\0' && *s2 == '\0')
		{
			return (0);
		}
		s1++;
		s2++;
		n--;
	}
	result = *s1 - *s2;
	return (result);
}
